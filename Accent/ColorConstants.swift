import UIKit

extension UIColor {
    
    class func accentLightColor() -> UIColor {
        return UIColor(red: 228/255, green: 237/255, blue: 242/255, alpha: 1)
    }
    
    class func accentDarkColor() -> UIColor {
        return UIColor(red: 50/255, green: 50/255, blue: 50/255, alpha: 1)
    }
    
    class func accentGrayColor() -> UIColor {
        return UIColor(red: 150/255, green: 150/255, blue: 150/255, alpha: 1)
    }
    
    class func accentBlueColor() -> UIColor {
        return UIColor(red: 178/255, green: 215/255, blue: 254/255, alpha: 1)
    }
}
